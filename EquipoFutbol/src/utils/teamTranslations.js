export const getTranslatedTeam = (team, language) => {
    if (language !== "en") {
        return team;
    }

    const translatedData = teamTranslations[team.name]?.en;

    if (!translatedData) {
        return {
            ...team,
            category: team.category === "PRIMERA DIVISIÓN" ? "FIRST DIVISION" : team.category,
            league: team.league === "Liga Profesional" ? "Professional League" : team.league
        };
    }

    return {
        ...team,
        ...translatedData
    };
};